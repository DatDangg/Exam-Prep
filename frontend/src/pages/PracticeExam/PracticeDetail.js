import { useLocation, useNavigate } from 'react-router-dom';
import styles from './PracticeDetail.module.css';
import { useAuth } from '../../hooks/useAuth';
import { useEffect, useState } from 'react';
import axios from 'axios';

function PracticeDetail() {
    const API = process.env.REACT_APP_API_URL;
    const location = useLocation();
    const { user } = useAuth();
    const [exam, setExam] = useState({})
    const [completed, setCompleted] = useState([])
    const examId = location.state?.examId || "";
    const navigate = useNavigate()

    useEffect(() => {
        axios.get(`${API}/exams/${examId}/detail`)
            .then(res => {
                setExam(res.data)
            })
            .catch(err => console.log(err))
    },[])

    useEffect(() => {
        if (!user) return; 

        axios.get(`${API}/exams/completed`, {
            params: { examId, userId: user.userId }
        })
        .then(res => {
            setCompleted(res.data)
        })
        .catch(err => console.log(err));
    }, [user]); 

    const handleClick = () => {
        navigate("/practice/test", {
            state: {examId, examName:exam.examName}
        })
    }

    return (
        <div className={styles.practice}>
            <div className='container'>
                <div className='row'>
                    <div className={styles.title}>{exam.examName}</div>
                </div>
                <div className='row'>
                    <div>L·ªãch s·ª≠ l√†m b√†i</div>
                    <table className={styles.table}>
                        <thead>
                            <tr>
                                <th className={styles.th}>STT</th>
                                <th className={styles.th}>Th·ªùi gian b·∫Øt ƒë·∫ßu</th>
                                <th className={styles.th}>Th·ªùi gian k·∫øt th√∫c</th>
                                <th className={styles.th}>ƒêi·ªÉm</th>
                                <th className={styles.th}>Xem l·∫°i</th>
                            </tr>
                        </thead>
                        <tbody>
                            {completed.map((exam, idx) => (
                                <tr key={idx}>
                                    <td className={styles.td}>{idx+1}</td>
                                    <td className={styles.td}>{exam.startTime}</td>
                                    <td className={styles.td}>{exam.endTime}</td>
                                    <td className={styles.td}>{exam.score}</td>
                                    <td className={styles.td}>üëÄ</td>
                                </tr>
                            ))}
                        </tbody>
                    </table>
                </div>
                <div className='row'>
                    <button onClick={handleClick}>L√†m b√†i</button>
                </div>
            </div>
        </div>
    );
}

export default PracticeDetail;
